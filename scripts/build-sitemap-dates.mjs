/**
 * build-sitemap-dates.mjs
 *
 * Generates src/data/sitemap-dates.ts — a map of sitemap route-group -> the ISO
 * date that group's content last actually changed, taken from the git commit
 * history of the group's source/data file. This is a REAL freshness signal
 * (when the data/template last changed), never a fabricated "today" stamp, so
 * the sitemap <lastmod> stays honest (Google distrusts always-current lastmod).
 *
 * Run manually after a data/content change, or via the `prebuild` npm hook.
 * SAFE ON SHALLOW CLONES: if git can't resolve a file's last commit (e.g. Vercel's
 * shallow checkout), that key is left to the committed snapshot value below —
 * the script never downgrades a real date to null or overwrites with "now".
 *
 *   node scripts/build-sitemap-dates.mjs
 */
import { execFileSync } from 'node:child_process'
import { readFileSync, writeFileSync, existsSync } from 'node:fs'

// route-group key -> the source/data file whose last change dates that group
const GROUPS = {
  home: 'src/app/(site)/page.tsx',
  calculatorsHub: 'src/app/(site)/calculators/page.tsx',
  calculators: 'src/components/calculators',
  lenders: 'src/data/lenders.ts',
  loanTypes: 'src/data/loan-types.ts',
  lenderComparisons: 'src/data/lender-comparisons.ts',
  marketStrategies: 'src/data/market-strategies.ts',
  capRateCities: 'src/data/cap-rate-cities.ts',
  usStates: 'src/data/us-states.ts',
  financingTypes: 'src/data/financing-types.ts',
  financingScenarios: 'src/data/financing-scenarios.ts',
  categoryContent: 'src/data/category-content.ts',
  reportInvestorFinancing: 'src/data/hmda-investor',
  reportRentalYield: 'src/data/rental-yield',
  reportInvestorLenders: 'src/data/hmda-lenders',
  reportRentGrowth: 'src/data/fmr-rent',
}

const OUT = 'src/data/sitemap-dates.ts'

function gitDate(file) {
  try {
    const out = execFileSync('git', ['log', '-1', '--format=%cI', '--', file], {
      encoding: 'utf8',
    }).trim()
    return out || null
  } catch {
    return null
  }
}

// Load the previous snapshot so shallow clones don't wipe real dates.
let prev = {}
if (existsSync(OUT)) {
  const m = readFileSync(OUT, 'utf8').match(/sitemapDates = (\{[\s\S]*?\}) as const/)
  if (m) {
    try {
      prev = JSON.parse(m[1].replace(/(\w+):/g, '"$1":').replace(/'/g, '"').replace(/,(\s*})/g, '$1'))
    } catch {
      prev = {}
    }
  }
}

const dates = {}
const notes = []
for (const [key, file] of Object.entries(GROUPS)) {
  const d = gitDate(file)
  if (d) {
    dates[key] = d.slice(0, 10) // YYYY-MM-DD — day precision is enough for lastmod
    notes.push(`${key}: ${dates[key]}`)
  } else if (prev[key]) {
    dates[key] = prev[key]
    notes.push(`${key}: ${prev[key]} (kept — git unavailable)`)
  } else {
    notes.push(`${key}: (no date — omitted)`)
  }
}

const body = `// AUTO-GENERATED by scripts/build-sitemap-dates.mjs — do not edit by hand.
// Per route-group last-content-change dates (from git history) for sitemap <lastmod>.
// Regenerate after data/content changes: node scripts/build-sitemap-dates.mjs

export const sitemapDates = ${JSON.stringify(
  Object.fromEntries(Object.entries(dates).sort()),
  null,
  2
)} as const

export type SitemapDateKey = keyof typeof sitemapDates

/** Real last-change date for a group as a Date, or undefined if unknown (omit lastmod — never fabricate). */
export function groupLastmod(key: SitemapDateKey): Date | undefined {
  const d = sitemapDates[key]
  return d ? new Date(d + 'T00:00:00Z') : undefined
}
`

writeFileSync(OUT, body)
console.log(`Wrote ${OUT}:\n  ${notes.join('\n  ')}`)
